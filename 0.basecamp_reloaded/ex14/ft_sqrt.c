int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (nb > 0)
	{
		nb = nb - i;
		i = i + 2;
	}
	if (nb == 0)
		return ((i - 1) / 2);
	else
		return (0);
}
